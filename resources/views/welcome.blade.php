<div class="w3-container w3padding-32" id="history">
   <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16">Brief History</h3>
        <p>
Eclaro Academy was established in 2013 as part of Eclaro Business Solution Inc.’s corporate social responsibility (CSR) initiatives. From a group of 18 technical-vocational scholars enrolled in the school under full subsidy from the business unit of Eclaro, the Eclaro Academy grew into a full-fledged academic institution offering pre-school, grade school and high school levels, as well as tertiary education courses in the fields of communication and information technology. Today, Eclaro Academy has more than 600 students and prides itself with all of its course offerings being recognized and accredited by the Department of Education (DepEd), Technical Education Skills Development Authority (TESDA), and the Commission on Higher Education.
        </p>
          <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16">A History of Success</h3>
        <p>
Eclaro is a Business and Technology Consulting Firm that delivers top talent to our clients globally across diverse industries. Founded in 1999, Eclaro is headquartered in New York City and operates in the United States, the Philippines, and Canada.

Eclaro’s management team averages over 30 years of experience in the staffing and consulting fields. We support clients of all sizes, and provide flexible solutions to solve our clients’ business problems. Eclaro recruits and manages a staff of highly skilled individuals in an array of specialized disciplines.  Eclaro’s relationships with some of the largest global companies has led to a 96% retention rate and an average partnership of over 10 years.
        </p>

