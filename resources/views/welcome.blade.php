  <div class="w3-container w3-padding-32" id="about">
    <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16">ABOUT ECLARO ACADEMY</h3>
    <p>
Eclaro Academy is a non-traditional, technology-oriented academic institution that adheres to student-centered pedagogical approach. The school believes in the philosophy of progressive learning which basically emphasizes exploration, creativity, critical thinking, creation of new knowledge, and the genuine concern for the experience of the student which allows every Eclaro enrollee to develop a well-rounded personality.
    </p>
     <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16">Vision</h3>
     <p>
      To be among Asia’s top academic institutions committed to nurture innovative learners and develop excellent leaders through technology-oriented, environmentally-sound, and globally-competitive education.
     </p>
      <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16">Mission</h3>
     <p>
To transform the knowledge, skills and attitudes of learners in order for them to become holistic. individuals and useful members of a fast-changing global community.
     </p>
        <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16">Government Licenses, Permits, and Accreditation</h3>
        <p>
          All course offerings of the Eclaro Academy strictly adhere to government standards and have the necessary permits, licenses, and accreditation from the Department of Education (DepEd), Technical Education Skills Development Authority (TESDA), and the Commission on Higher Education (CHED).
        </p>
        <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16">Competitive Advantage of Eclaro Education</h3>
        <p>
         Every course curriculum of Eclaro Academy is designed to meet global standards of academic excellence. Eclaro education emphasizes life-long learning, critical thinking, scientific inquiry, intellectual curiosity, creativity, and continuous improvement. The Academy also spends considerable time to ensure that its students are able to acquire practical experience in actual industry settings so that they gain knowledge and experience which are responsive to the needs and requirements of their future employers.
        </p>
          <h3 class="w3-border-bottom w3-border-light-grey w3-padding-16">Mentoring System</h3>
        <p>
         At Eclaro Academy, the students learn and acquire skills that are useful in their future employment. They are particularly trained to gain highly-employable knowledge and skills required in the actual workplace. The Academy does this through the unique Eclaro Mentoring System (EMS) which has defined the Eclaro way of learning. Every student is assigned to a senior academic adviser who is responsible to provide academic support and intellectual guidance to the assigned Eclaro student so that critical skills, knowledge, professional attitudes, and work ethics are honed through rigid training and practice.
        </p>
